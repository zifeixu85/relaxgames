// Function to get data for a specific game
function getGameData(gameSlug) {
  return gameData[gameSlug] || null;
}

// Function to add enhanced content to game pages
function enhanceGamePage() {
  // Extract game slug from URL
  const pathSegments = window.location.pathname.split('/');
  const gameFileName = pathSegments[pathSegments.length - 1];
  const gameSlug = gameFileName.replace('.html', '');
  
  // Get game data
  const data = getGameData(gameSlug);
  if (!data) return;
  
  // Find the game container section
  const gameContainer = document.querySelector('.game-container') || 
                       document.querySelector('.game-iframe-container')?.parentNode;
  
  if (!gameContainer) return;
  
  // Create enhanced content section
  const enhancedSection = document.createElement('div');
  enhancedSection.className = 'enhanced-content mt-8 p-6 bg-white rounded-xl shadow-md';
  
  // Add game description
  const descriptionEl = document.createElement('div');
  descriptionEl.className = 'game-description mb-6';
  descriptionEl.innerHTML = `
    <h2 class="text-xl font-bold mb-3">About ${data.title.split(' - ')[0]}</h2>
    <p class="text-gray-700">${data.description}</p>
  `;
  enhancedSection.appendChild(descriptionEl);
  
  // Add how to play section
  if (data.howToPlay && data.howToPlay.length) {
    const howToPlayEl = document.createElement('div');
    howToPlayEl.className = 'how-to-play mb-6';
    howToPlayEl.innerHTML = `
      <h2 class="text-xl font-bold mb-3">How to Play</h2>
      <ul class="list-disc pl-5 space-y-1">
        ${data.howToPlay.map(step => `<li class="text-gray-700">${step}</li>`).join('')}
      </ul>
    `;
    enhancedSection.appendChild(howToPlayEl);
  }
  
  // Add FAQs
  if (data.faqs && data.faqs.length) {
    const faqEl = document.createElement('div');
    faqEl.className = 'faq-section';
    faqEl.innerHTML = `
      <h2 class="text-xl font-bold mb-3">Frequently Asked Questions</h2>
      <div class="space-y-4">
        ${data.faqs.map(faq => `
          <div class="faq-item">
            <h3 class="font-bold text-gray-800">${faq.question}</h3>
            <p class="text-gray-700">${faq.answer}</p>
          </div>
        `).join('')}
      </div>
    `;
    enhancedSection.appendChild(faqEl);
  }
  
  // Add the enhanced section after the game container
  gameContainer.parentNode.insertBefore(enhancedSection, gameContainer.nextSibling);
  
  // Update meta tags
  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) {
    metaDescription.setAttribute('content', data.description);
  }
  
  const metaKeywords = document.querySelector('meta[name="keywords"]');
  if (metaKeywords) {
    metaKeywords.setAttribute('content', data.keywords);
  }
  
  // Update title if needed
  const titleTag = document.querySelector('title');
  if (titleTag && !titleTag.textContent.includes(data.title.split(' - ')[0])) {
    titleTag.textContent = data.title + ' | RelaxGames.online';
  }
}

// Run the enhancement when DOM is loaded
document.addEventListener('DOMContentLoaded', enhanceGamePage); 